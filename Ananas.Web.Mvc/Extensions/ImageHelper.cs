                        {
                            fromR.Y = (int)System.Math.Floor((initImage.Height - initImage.Width / templateRate) / 2);//居中

                        }
                        else
                        {
                            if (type == 1)
                                fromR.Y = 0;//上
                            else
                                fromR.Y = (int)System.Math.Floor((initImage.Height - initImage.Width / templateRate));//下
                        }

                        fromR.Width = initImage.Width;
                        fromR.Height = (int)System.Math.Floor(initImage.Width / templateRate);

