        /// 增加文字水印
        /// </summary>
        /// <param name="fileSaveUrl">文件名</param>
        /// <param name="watermarkText">水印文字</param>
        /// <param name="watermarkStatus">图片水印位置 0=不使用 1=左上 2=中上 3=右上 4=左中 5=正中 6=右中 7=左下 8=中下 9=右下</param>
