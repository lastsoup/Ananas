                    templateG.SmoothingMode = System.Drawing.Drawing2D.SmoothingMode.HighQuality;
                    templateG.PixelOffsetMode = System.Drawing.Drawing2D.PixelOffsetMode.Half;
                    templateG.Clear(Color.Transparent);
